const theoryByTitleFallback: Record<string, string> = {
  'Гражданское право': 'grazhdanskoe-pravo',
  Экономика: 'ekonomika',
  Конституция: 'konstituciya',
  Социология: 'sociologiya',
};

export function resolveTheoryTopic(category: { slug?: string; title?: string } | null | undefined): TheoryTopic {
  const slug = category?.slug || '';
  if (slug && theoryBySlug[slug]) {
    return theoryBySlug[slug];
  }

  const byTitle = category?.title ? theoryByTitleFallback[category.title] : undefined;
  if (byTitle && theoryBySlug[byTitle]) {
    return theoryBySlug[byTitle];
  }

  return theoryBySlug.konstituciya;
}

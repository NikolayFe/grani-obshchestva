async function main() {
  console.log('Начало заполнения базы данных...');

  // Удалить существующие данные
  await prisma.term.deleteMany();
  await prisma.category.deleteMany();
  console.log('Старые данные удалены');

  for (const cat of categories) {
    const allTerms = [...termsByCategory[cat.slug], ...additionalTermsByCategory[cat.slug]];

    const created = await prisma.category.create({
      data: {
        slug: cat.slug,
        title: cat.title,
        color: cat.color,
        terms: {
          create: allTerms.map((t, i) => ({
            term: t.term,
            definition: t.definition,
            isNew: i < 5, // первые 5 терминов каждой категории — новые
          })),
        },
      },
    });
    console.log(`✓ Создана категория "${created.title}" с ${allTerms.length} терминами`);
  }

  console.log('\nГотово! Данные в базе:');
  const count = await prisma.term.count();
  console.log(`Всего терминов: ${count}`);
}


function mapCuratedQuestion(rawQuestion) {
  const [text, correct, wrong1, wrong2, wrong3] = rawQuestion;
  return {
    text,
    source: 'category',
    difficulty: 1,
    options: [
      { text: correct, isCorrect: true },
      { text: wrong1, isCorrect: false },
      { text: wrong2, isCorrect: false },
      { text: wrong3, isCorrect: false },
    ],
  };
}

function buildTermConceptQuestions(allTerms, limit) {
  const questions = [];
  const pool = allTerms.slice(0, Math.max(limit, 10));

  for (let i = 0; i < limit; i += 1) {
    const current = pool[i % pool.length];
    const wrong1 = pool[(i + 1) % pool.length];
    const wrong2 = pool[(i + 2) % pool.length];
    const wrong3 = pool[(i + 3) % pool.length];
    const definition = current.definition.replace(/\.$/, '');

    questions.push({
      text: `Какой термин точнее всего соответствует определению: "${definition}"?`,
      source: 'category',
      difficulty: 1,
      options: [
        { text: current.term, isCorrect: true },
        { text: wrong1.term, isCorrect: false },
        { text: wrong2.term, isCorrect: false },
        { text: wrong3.term, isCorrect: false },
      ],
    });
  }

  return questions;
}

function buildCategoryQuestions(categorySlug, allTerms) {
  const curated = (curatedCategoryQuestionBanks[categorySlug] || []).map(mapCuratedQuestion);

  if (curated.length >= 50) {
    return curated.slice(0, 50);
  }

  const generated = buildTermConceptQuestions(allTerms, 50 - curated.length);
  return [...curated, ...generated].slice(0, 50);
}

function buildGlossaryQuestions(allTerms) {
  const questions = [];
  const termsForQuestions = allTerms.slice(0, 50);

  for (let i = 0; i < termsForQuestions.length; i += 1) {
    const current = termsForQuestions[i];
    const wrong1 = termsForQuestions[(i + 1) % termsForQuestions.length];
    const wrong2 = termsForQuestions[(i + 2) % termsForQuestions.length];
    const wrong3 = termsForQuestions[(i + 3) % termsForQuestions.length];

    if (i % 2 === 0) {
      questions.push({
        text: `Что означает термин \"${current.term}\"?`,
        source: 'glossary_term',
        difficulty: 3,
        options: [
          { text: current.definition, isCorrect: true },
          { text: wrong1.definition, isCorrect: false },
          { text: wrong2.definition, isCorrect: false },
          { text: wrong3.definition, isCorrect: false },
        ],
      });
    } else {
      const definition = current.definition.replace(/\.$/, '');
      questions.push({
        text: `Какой термин соответствует определению: \"${definition}\"?`,
        source: 'glossary_term',
        difficulty: 3,
        options: [
          { text: current.term, isCorrect: true },
          { text: wrong1.term, isCorrect: false },
          { text: wrong2.term, isCorrect: false },
          { text: wrong3.term, isCorrect: false },
        ],
      });
    }
  }

  return questions;
}

async function main() {
  console.log('Начало заполнения базы данных...');

  // Проверяем, есть ли уже данные — если да, пропускаем очистку
  const existingCats = await prisma.category.count();
  const existingTerms = await prisma.term.count();
  const existingQuestions = await prisma.question.count();

  const EXPECTED_CATS = categories.length;           // 4
  const EXPECTED_TERMS = 400;
  const EXPECTED_QUESTIONS = 400;

  if (
    existingCats === EXPECTED_CATS &&
    existingTerms >= EXPECTED_TERMS &&
    existingQuestions >= EXPECTED_QUESTIONS
  ) {
    console.log(`✓ Данные уже есть (${existingCats} кат., ${existingTerms} терм., ${existingQuestions} вопр.) — пропускаем заполнение.`);
    return;
  }

  // Удаляем сначала зависимые тестовые данные.
  await prisma.userAnswer.deleteMany();
  await prisma.userCategoryTestProgress.deleteMany();
  await prisma.dailyTestQuestion.deleteMany();
  await prisma.questionOption.deleteMany();
  await prisma.question.deleteMany();
  await prisma.userTermProgress.deleteMany();

  // Удалить существующие категории и термины
  await prisma.term.deleteMany();
  await prisma.category.deleteMany();
  console.log('Старые данные удалены');

  for (const cat of categories) {
    const allTerms = [...termsByCategory[cat.slug], ...additionalTermsByCategory[cat.slug]];
    const categoryQuestions = buildCategoryQuestions(cat.slug, allTerms);
    const glossaryQuestions = buildGlossaryQuestions(allTerms);
    const allQuestions = [...categoryQuestions, ...glossaryQuestions];

    const created = await prisma.category.create({
      data: {
        slug: cat.slug,
        title: cat.title,
        color: cat.color,
        terms: {
          create: allTerms.map((t, i) => ({
            term: t.term,
            definition: t.definition,
            isNew: i < 5, // первые 5 терминов каждой категории — новые
          })),
        },
        questions: {
          create: allQuestions.map((q) => ({
            text: q.text,
            difficulty: q.difficulty || 1,
            source: q.source,
            options: {
              create: q.options,
            },
          })),
        },
      },
    });
    console.log(`✓ Создана категория "${created.title}" с ${allTerms.length} терминами и ${allQuestions.length} вопросами`);
  }

  console.log('\nГотово! Данные в базе:');
  const termsCount = await prisma.term.count();
  const questionsCount = await prisma.question.count();
  const optionsCount = await prisma.questionOption.count();
  console.log(`Всего терминов: ${termsCount}`);
  console.log(`Всего вопросов: ${questionsCount}`);
  console.log(`Всего вариантов ответов: ${optionsCount}`);
}

